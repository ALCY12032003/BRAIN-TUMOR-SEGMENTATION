    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try
        {
            int unitsPerPixel =1;
             
            File fe=new File("Norm");
            File lt[]=fe.listFiles();
            
            Imageset imgset=new Imageset();
            
            for(int i=0;i<lt.length;i++)
            {
                BufferedImage bi=ImageIO.read(lt[i]);
                ImageInst ins=new ImageInst(bi);
                imgset.add(ins);
            }
            
            ImageInst sampleImage = imgset.getImages().get(0); 
            int inVecSize = sampleImage.getWidth() * sampleImage.getHeight() * unitsPerPixel + 1;
            Vector<Vector<Double>> trainFeatVec = new Vector<Vector<Double>>(imgset.getSize());
            
            for (ImageInst image : imgset.getImages()) 
            {
                trainFeatVec.addElement(convertToFeatureVector(image,inVecSize));
            }
            
            
            int countActivationMapsConv = 10;
            int kernelSizeConv          = 5;
            int countClasses            = 2;
            int padding                 = 0;
            int stride                  = 1;

            int hparam  = ( (padding <<28)|(countClasses<<24) |(stride <<16) | (kernelSizeConv << 8) | countActivationMapsConv )  ;
            //System.out.println(hparam);
            
            ConvNet bat = new ConvNet(trainFeatVec , hparam, false);
            bat.trainCNN(trainFeatVec);
            
            JOptionPane.showMessageDialog(this, "Bat  Training Completed....");
            jButton4.setEnabled(true);
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

