    private static Vector<Double> convertToFeatureVector(ImageInst image,int inVecSize) 
    {
        Vector<Double> result = new Vector<Double>(inVecSize);

        for (int index = 0; index < inVecSize - 1; index++) 
        {             
            int xValue = index % image.getWidth();
            int yValue = index / image.getWidth();
            result.add(image.getGrayImage()   [xValue][yValue] / 255.0);
            
        }
        //  result.add((double) convertCategoryStringToEnum(image.getLabel()).ordinal()); 

        return result;
    }

